package exercisew35;

public class Calculations {
    static double add(double num1, double num2) {
        return num1 + num2;
    }
    static double sub(double num1, double num2) {
        return num1 - num2;
    }
    static double multiply(double num1, double num2) {
        return num1 * num2;
    }
    static double div(double num1, double num2) {
        return num1 / num2;
    }
    
}
